names = ["Dave", "Sara", "John"]

# Print each name
for n in names:
    print(n)

print("\n")

# Loop through a string
for ch in "Mississippi":
    print(ch)

print("\n")

# Skip a value with continue
for n in names:
    if n == "Sara":
        continue
    print(n)

print("\n")

